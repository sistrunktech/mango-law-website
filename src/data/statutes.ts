export function getStatutesByPracticeArea(practiceArea: string): OhioStatute[] {
  const statuteSections = practiceAreaStatutes[practiceArea as keyof typeof practiceAreaStatutes] || [];
  return statuteSections.map(section => ohioStatutes[section]).filter(Boolean);
}

export function getStatute(section: string): OhioStatute | undefined {
  return ohioStatutes[section];
}

export function getRelatedStatutes(section: string): OhioStatute[] {
  const statute = ohioStatutes[section];
  if (!statute || !statute.relatedStatutes) return [];
  return statute.relatedStatutes.map(s => ohioStatutes[s]).filter(Boolean);
}

export function getAllStatutes(): OhioStatute[] {
  return Object.values(ohioStatutes);
}
